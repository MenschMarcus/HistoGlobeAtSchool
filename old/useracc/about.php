    <h2>What is HistoGlobe?</h2>
    <p>HistoGlobe is a free Web Service to visualise historical events and geopolitical changes on earth. The goal is to create a teaching and learning tool in one that replaces all traditional maps used in history lessons. The interface, a web site, consists of a 2D world map and a scrollable time line. These allow the user to select a time and place on earth in order to see the state of that region at that moment in history, such as borders, names of countries and regions and historical events. And the best thing: it is available from everywhere - with a browser.</p>

    <h2>Whom is HistoGlobe for?</h2>
    <p>In principle, HistoGlobe is for everybody who is interested in history, politics, geography and likes to use the advantages of the digitized world. However, the service is especially made for teaching and learning about history in general. It got created in order to make life as a teacher or lecturer of history easier and better. </p>
    <p>Also for any kind of learners of history. HistoGlobe shall help to get a visual overview about history and provide free access to historical information on a map and on a timeline. It is not designed to replace history books, but it may support an understanding of the impact and the coherences of and between historical events.</p>
    
    <h2>Where does HistoGlobe come from?</h2>
    <p>The idea was born in 2008, when I was still a student of the "Erwin-Strittmatter-Gymnasium" (High School) in Spremberg, Germany. My intensive course history was one of the best learning subjects I ever had. Our teacher Frank Berno forced us to developed schemas to understand history better. They helped me a lot to understand history better. Also historical maps give you a good overview about history. However, these schemas were all static. They did not move.</p>
    <p>Now, almost four years later after one year of travelling through the world and three years of studying computer science and media I know that dynamic content helps you even more. In autumn 2010, during my studies at Bauhaus-University in Weimar, Germany, I developed the theoretic idea about "HistoGlobe" in a course "Foundations of Software Engineering". I dealed with all kinds of UML diagrams and software architecture, but the main part was missing: The real software.</p>
    <p>In autumn 2011 I started an exchange semester at the University of Turku, Finland. In a project course hosted by the swedish-speaking Åbo Akademi in Turku I finally got the chance to implement the system with a project group of six people, based on the historical information of the Nordic countries from 1800 until today. Especially due to the knowledge, commitment and patience of Frej Bjon, HistoGlobe was born in the cold Finnish winter 2011/2012. And now it is there!</p>
    <p>However, the work is not done. I will continue to work and improve HistoGlobe and am happy about everybody who will support me in that.</p>
    
